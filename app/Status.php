<?php namespace App;

use Illuminate\Database\Eloquent\Model;

class Status extends Model {

    /**
     * @var array
     */
    protected $fillable = ['name'];

    /**
     * @return \Illuminate\Database\Eloquent\Relations\HasMany
     */
    public function projects()
    {
        return $this->hasMany('App\Project');
    }

}
